aventuras = {}

def ganhar_xp(user_id, quantidade):
    player = aventuras[user_id]
    player['xp'] += quantidade
    
    xp_necessario = player['level'] * 10
    if player['xp'] >= xp_necessario:
        player['level'] += 1
        player['xp'] = 0
        return True
    return False

